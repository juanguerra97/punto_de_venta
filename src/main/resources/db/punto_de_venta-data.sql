-- Se calcula el total de cada venta
DECLARE
    total ventas.total_venta%TYPE;
    venta ventas%ROWTYPE;
    
    CURSOR CursorVentas IS
	  SELECT *
	  FROM ventas
	FOR UPDATE OF total_venta;
    
BEGIN
    IF NOT CursorVentas%ISOPEN THEN
        OPEN CursorVentas;
    END IF;
    
    LOOP
        FETCH CursorVentas INTO venta;
        EXIT WHEN CursorVentas%NOTFOUND;
        
        SELECT SUM(precio * cantidad) INTO total FROM detalle_venta WHERE numero_venta = venta.numero_venta;
        
        UPDATE ventas SET total_venta = total WHERE CURRENT OF CursorVentas;
    END LOOP;
    
    IF CursorVentas%ISOPEN THEN
        CLOSE CursorVentas;
    END IF;
END;
/

-- Se calcula el total de cada compra
DECLARE
    total compras.total_compra%TYPE;
    compra compras%ROWTYPE;
    
    CURSOR CursorCompras IS
	  SELECT *
	  FROM compras
	FOR UPDATE OF total_compra;
    
BEGIN
    IF NOT CursorCompras%ISOPEN THEN
        OPEN CursorCompras;
    END IF;
    
    LOOP
        FETCH CursorCompras INTO compra;
        EXIT WHEN CursorCompras%NOTFOUND;
        
        SELECT SUM(costo * cantidad) INTO total FROM detalle_compra WHERE numero_compra = compra.numero_compra;
        
        UPDATE compras SET total_compra = total WHERE CURRENT OF CursorCompras;
    END LOOP;
    
    IF CursorCompras%ISOPEN THEN
        CLOSE CursorCompras;
    END IF;
END;
/


